package hu.sze.jkk.auto.track.mapgeneration;

public class ControlVertex {
	private final double x;
	private final double y;
	private final double z;
	
	public ControlVertex(double x, double y, double z) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	public double getZ() {
		return z;
	}
	
	
}
